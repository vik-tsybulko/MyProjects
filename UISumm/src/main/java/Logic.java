public class Logic {
    public int summ (int a, int b){
        return a + b;
    }
    public int razn (int a, int b){
        return a - b;
    }
    public int umn (int a, int b){
        return a * b;
    }
    public int pod (int a, int b){
        return a / b;
    }
}
